#include "fillit.h"

void	clear_grid(t_grid **grid)
{
	int	x;
	int	y;

	y = 0;
	while (y < (*grid)->max_size)
	{
		x = 0;
		while (x < (*grid)->max_size)
		{
			(*grid)->cells[y][x] = '.';
			x++;
		}
		y++;
	}
}
