#include "fillit.h"

int		can_put(t_grid *g, t_point *p, t_tetri *t)
{
	int i;

	i = 0;
	while (i < 4)
	{
		if (g->cells[p->y + t->points[i]->y][p->x + t->points[i]->x] != '.')
			return (0);
		i++;
	}
	return (1);
}
